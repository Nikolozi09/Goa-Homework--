# ამოცანა 1
age = int(input("Enter your age: "))  # მომხმარებლის ასაკი ცვლად age-ში
age_minus_two = age - 2  # ასაკიდან 2-ის გამოკლება
print("Your age minus 2 is:", age_minus_two)

# ამოცანა 2
num1 = int(input("Enter your num1: "))  # პირველი რიცხვის მიღება
num2 = int(input("Enter your num2: "))  # მეორე რიცხვის მიღება

# მათემატიკური ოპერაციები
sum_result = num1 + num2
difference = num1 - num2
product = num1 * num2
quotient = num1 / num2

# შედეგების გამოტანა
print("sum of numbers:", sum_result)
print("difference of numbers:", difference)
print("product of numbers:", product)
print("divisor of numbers:", quotient)

