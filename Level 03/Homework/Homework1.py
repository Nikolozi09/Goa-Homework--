# 1. გაერთიანება (+)
sum_result = 5 + 3
print(f"5 + 3 = {sum_result}")

# 2. გამოკლება (-)
difference = 10 - 4
print(f"10 - 4 = {difference}")

# 3. გამამრავლება (*)
multiplication = 6 * 7
print(f"6 * 7 = {multiplication}")

# 4. გაყოფა (//)
integer_division = 20 // 3
print(f"20 // 3 = {integer_division}")

# 5. მეასე გაყოფა (/)
division = 20 / 3
print(f"20 / 3 = {division}")

# 6. ძალისხმევა (**)
exponentiation = 2 ** 3
print(f"2 ** 3 = {exponentiation}")

# 7. გაზრდილი გამრავლება (+ და *)
compound_multiplication = 3 + 2 * 2
print(f"3 + 2 * 2 = {compound_multiplication}")

# 8. ნაკლებობა გაყოფით
subtract_and_divide = (5 - 2) / 3
print(f"(5 - 2) / 3 = {subtract_and_divide}")

# 9. გაზრდილი გამოკლება
compound_subtraction = 10 - 3 - 2
print(f"10 - 3 - 2 = {compound_subtraction}")

# 10. გაყოფის შემდეგ განახლება
compound_division = 20 / 5 * 3
print(f"20 / 5 * 3 = {compound_division}")
